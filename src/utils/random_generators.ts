export function generate_random_username(): string {
  const adjective =
    USERNAME_ADJECTIVES[Math.floor(Math.random() * USERNAME_ADJECTIVES.length)];
  const noun =
    USERNAME_NOUNS[Math.floor(Math.random() * USERNAME_NOUNS.length)];
  const numbers = Math.floor(Math.random() * 9000 + 1000);

  return `${adjective}${noun}${numbers}`;
}

export function generate_random_display_name(): string {
  const adjective_index = Math.floor(Math.random() * DISPLAY_ADJECTIVES.length);
  let noun_index = Math.floor(Math.random() * DISPLAY_NOUNS.length);

  const adjective = DISPLAY_ADJECTIVES[adjective_index];
  let noun = DISPLAY_NOUNS[noun_index];

  if (adjective.charAt(0).toLowerCase() === noun.charAt(0).toLowerCase()) {
    noun_index = (noun_index + 1) % DISPLAY_NOUNS.length;
    noun = DISPLAY_NOUNS[noun_index];
  }

  return `${adjective} ${noun}`;
}
